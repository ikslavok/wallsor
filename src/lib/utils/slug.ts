    'г': 'g', 'Г': 'G', 'д': 'd', 'Д': 'D', 'ђ': 'dj', 'Ђ': 'Dj',
    'е': 'e', 'Е': 'E', 'ж': 'z', 'Ж': 'Z', 'з': 'z', 'З': 'Z',
    'и': 'i', 'И': 'I', 'ј': 'j', 'Ј': 'J', 'к': 'k', 'К': 'K',
    'л': 'l', 'Л': 'L', 'љ': 'lj', 'Љ': 'Lj', 'м': 'm', 'М': 'M',
    'н': 'n', 'Н': 'N', 'њ': 'nj', 'Њ': 'Nj', 'о': 'o', 'О': 'O',
    'п': 'p', 'П': 'P', 'р': 'r', 'Р': 'R', 'с': 's', 'С': 'S',
    'т': 't', 'Т': 'T', 'ћ': 'c', 'Ћ': 'C', 'у': 'u', 'У': 'U',
    'ф': 'f', 'Ф': 'F', 'х': 'h', 'Х': 'H', 'ц': 'c', 'Ц': 'C',
    'ч': 'c', 'Ч': 'C', 'џ': 'dz', 'Џ': 'Dz', 'ш': 's', 'Ш': 'S',
    
    // Latin with diacritics
    'á': 'a', 'à': 'a', 'ä': 'a', 'â': 'a', 'ā': 'a', 'ã': 'a', 'å': 'a', 'ą': 'a',
    'Á': 'A', 'À': 'A', 'Ä': 'A', 'Â': 'A', 'Ā': 'A', 'Ã': 'A', 'Å': 'A', 'Ą': 'A',
    'ć': 'c', 'č': 'c', 'ç': 'c', 'ĉ': 'c', 'ċ': 'c',
    'Ć': 'C', 'Č': 'C', 'Ç': 'C', 'Ĉ': 'C', 'Ċ': 'C',
    'ď': 'd', 'đ': 'd', 'ð': 'd',
    'Ď': 'D', 'Đ': 'D', 'Ð': 'D',
    'é': 'e', 'è': 'e', 'ë': 'e', 'ê': 'e', 'ē': 'e', 'ė': 'e', 'ę': 'e', 'ě': 'e',
    'É': 'E', 'È': 'E', 'Ë': 'E', 'Ê': 'E', 'Ē': 'E', 'Ė': 'E', 'Ę': 'E', 'Ě': 'E',
    'í': 'i', 'ì': 'i', 'ï': 'i', 'î': 'i', 'ī': 'i', 'į': 'i',
    'Í': 'I', 'Ì': 'I', 'Ï': 'I', 'Î': 'I', 'Ī': 'I', 'Į': 'I',
    'ľ': 'l', 'ĺ': 'l', 'ļ': 'l', 'ł': 'l',
    'Ľ': 'L', 'Ĺ': 'L', 'Ļ': 'L', 'Ł': 'L',
    'ń': 'n', 'ň': 'n', 'ñ': 'n', 'ņ': 'n',
    'Ń': 'N', 'Ň': 'N', 'Ñ': 'N', 'Ņ': 'N',
    'ó': 'o', 'ò': 'o', 'ö': 'o', 'ô': 'o', 'ō': 'o', 'õ': 'o', 'ø': 'o', 'ő': 'o',
    'Ó': 'O', 'Ò': 'O', 'Ö': 'O', 'Ô': 'O', 'Ō': 'O', 'Õ': 'O', 'Ø': 'O', 'Ő': 'O',
    'ŕ': 'r', 'ř': 'r', 'ŗ': 'r',
    'Ŕ': 'R', 'Ř': 'R', 'Ŗ': 'R',
    'ś': 's', 'š': 's', 'ş': 's', 'ŝ': 's', 'ș': 's',
    'Ś': 'S', 'Š': 'S', 'Ş': 'S', 'Ŝ': 'S', 'Ș': 'S',
    'ť': 't', 'ţ': 't', 'ț': 't',
    'Ť': 'T', 'Ţ': 'T', 'Ț': 'T',
